import jouvieje.bass.defines.BASS_DATA

enum class FFTMode(val bins: Int, val bassInt: Int) {
    FFT256(128, BASS_DATA.BASS_DATA_FFT256),
    FFT512(256, BASS_DATA.BASS_DATA_FFT512),
    FFT1024(512, BASS_DATA.BASS_DATA_FFT1024),
    FFT2048(1024, BASS_DATA.BASS_DATA_FFT2048),
    FFT4096(2048, BASS_DATA.BASS_DATA_FFT4096),
    FFT8192(4096, BASS_DATA.BASS_DATA_FFT8192),
    FFT16384(8192, BASS_DATA.BASS_DATA_FFT16384)
}